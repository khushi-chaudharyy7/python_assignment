#Checking if a string starts with a prefix

my_string = "Hello, world!"
prefix = "Hello"

if my_string.startswith(prefix):
    print("The string starts with the specified prefix.")
else:
    print("The string does not start with the specified prefix.")


#Checking if a string ends with a suffix

my_string = "Hello, world!"
suffix = "world!"

if my_string.endswith(suffix):
    print("The string ends with the specified suffix.")
else:
    print("The string does not end with the specified suffix.")